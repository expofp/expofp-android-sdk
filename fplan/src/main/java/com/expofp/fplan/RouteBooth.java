package com.expofp.fplan;

public class RouteBooth {
    private String id;
    private String name;

    public RouteBooth(String id, String name){
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return this.id;
    }

    public String getName() {
        return this.name;
    }
}
